#include <stdio.h>

int main()
{
  /* Declare variables.  */
  int value1, value2, sum;

  /* assign values to variables */
  value1 = 50;
  value2 = 25;

  /* compute the sum */
  sum = value1 + value2;

  /* Display the results to the screen */
  printf("The sum of %i and %i is %i\n", value1, value2, sum);
  return 0;
}